subroutine SC_PackParam(Buf, Indata)
   type(PackBuffer), intent(inout) :: Buf
   type(SC_ParameterType), intent(in) :: InData
   character(*), parameter         :: RoutineName = 'SC_PackParam'
   logical         :: PtrInIndex
